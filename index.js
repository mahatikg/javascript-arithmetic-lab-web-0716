function add(a,b){
  return a + b;
}

function subtract(a,b){
  return a - b;
}

function multiply(a,b){
  return a * b;
}

function divide(a,b){
  return a / b;
}

function inc(n){
  return ++n;
}
// console.log(inc(10))

function dec(n){
  return --n;
}

function makeInt(n){

  return parseInt(n, 10);

}
//
 // console.log(makeInt(30.12))

function preserveDecimal(n){

  return parseFloat(n);

}

